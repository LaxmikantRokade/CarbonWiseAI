export function calculateSavings(scenario, value) {
  const annualCO2 = value * scenario.savingsPerUnit * scenario.annualMultiplier;
  const trees = annualCO2 * scenario.treesEquivalent;
  const money = value * scenario.moneySaved * scenario.annualMultiplier;

  return {
    dailyCO2: (annualCO2 / 365).toFixed(2),
    weeklyCO2: (annualCO2 / 52).toFixed(1),
    monthlyCO2: (annualCO2 / 12).toFixed(1),
    annualCO2: annualCO2.toFixed(0),
    trees: Math.round(trees),
    moneySaved: money.toFixed(0),
  };
}
